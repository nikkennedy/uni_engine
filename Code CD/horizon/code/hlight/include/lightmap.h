#ifndef _LIGHTMAP_H_
#define _LIGHTMAP_H_

// --------------------------------------------------------------------------------- 
// CLightMap
// --------------------------------------------------------------------------------- 
class CLightMap {
public:
	
	// --------------------------------------------------------------------------------- 
	// lightmap image
	// --------------------------------------------------------------------------------- 
	u_char *rgb_data;


	// --------------------------------------------------------------------------------- 
	// lightmap dimensions
	// --------------------------------------------------------------------------------- 
	int  width;
	int  height;

	// --------------------------------------------------------------------------------- 
	// stored for use whilst lightmap processing
	// --------------------------------------------------------------------------------- 
	CVert3D edge1;
	CVert3D edge2;
	CVert3D uv_vector;

	// --------------------------------------------------------------------------------- 
	// constructor
	// --------------------------------------------------------------------------------- 
	CLightMap() 
	{
		width  = 16;
		height = 16;
	}

	// --------------------------------------------------------------------------------- 
	// lightmap texture coordinates
	// --------------------------------------------------------------------------------- 
	float u[3]; // lightmap per poly
	float v[3];
};


#endif

